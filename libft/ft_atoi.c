#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while ((*str == '\t' || *str == '\n' || *str == '\v'
			|| *str == '\f' || *str == '\r' || *str == ' ') && *str)
		str++;
	if ((*str == '+' || *str == '-') && *str)
	{
		if (*str == '-')
			i++;
		str++;
	}
	while ((*str >= '0' && *str <= '9') && *str)
	{
		n = n * 10 + ((int)*str - 48);
		str++;
	}
	if (i == 1)
		n = -n;
	return (n);
}
